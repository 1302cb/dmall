package cn.yusei.dmall.user.utils;
/**
 * 这就就是一个简单的返回信息的类
 * @author 11366
 *
 */
public class CodeResult {

	private String code;

	public CodeResult(String code) {
		super();
		this.code = code;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}
	
}
